/*Crie um laço while que irá iterar (repetir) enquanto n for menor
ou igual a 5. Mostre no console os valores obtidos.*/

let n = 0; // Inicializo o valor

while (n <= 5) { // Enquanto n for menor ou igual a 5
    console.log("O valor de n é: " + n); // Exibe o valor de n no console
    n++;
}